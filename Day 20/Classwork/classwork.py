number = int(input("შეიყვანე რიცხვი: "))

if number < 15:
    print("რიცხვი ნაკლებია 15-ზე")
else:
    print("რიცხვი მეტია 15-ზე")


age = int(input("შეიყვანე შენი ასაკი: "))

